//You can use the pop() method to remove the last element from an array.

let dailyActivities = ['MO','riyaj','devloper'];

// remove the last element
dailyActivities.pop();
console.log(dailyActivities); 

// remove the last element from ['MO', 'riyaj', 'devloper']
const removedElement = dailyActivities.pop();

//get removed element
console.log(removedElement); 
console.log(dailyActivities); 





//the shift() is use to remove first element in the array



let dailyactivities = ['work', 'eat', 'sleep'];

// remove the first element
dailyActivities.shift();

